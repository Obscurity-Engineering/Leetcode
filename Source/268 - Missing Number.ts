/**
 * {@link https://leetcode.com/problems/missing-number/}
 * 
 * Given an array `nums` containing `n` distinct numbers in the range `[0, n]`,
 * return *the only number in the range that is missing from the array*.
 * 
 * Constraints:
 * - `n == nums.length`
 * - `1 <= n <= 10^4`
 * - `0 <= nums[i] <= n`
 * - All the numbers of `nums` are **unique**.
 */
export type MissingNumber = (nums: number[]) => number;

export function missingNumber_Grey(nums: number[]): number {
  nums;
  return 0;
}

export function missingNumber_Zach(nums: number[]): number {
  nums;
  return 0;
}